# Numbers in Python
# Python supports several types of numbers: integers, floating-point numbers, and complex numbers.

# 1. Integer (int): Whole numbers, positive or negative, without decimals.
num1 = 10
num2 = -5
print("Integer examples:", num1, num2)

# 2. Floating-point (float): Numbers with decimal points.
num3 = 3.14
num4 = -0.001
print("Float examples:", num3, num4)

# 3. Complex numbers: Numbers with a real and imaginary part.
num5 = 2 + 3j
num6 = -1j
print("Complex number examples:", num5, num6)

# You can use type() to check the type of a number:
print(type(num1))  # <class 'int'>
print(type(num3))  # <class 'float'>
print(type(num5))  # <class 'complex'>


