# Comparison Operators in Python
# Comparison operators are used to compare values and return True or False.

x = 10
y = 5

# 1. Equal to (==)
print("x == y:", x == y)  # False
print(10 == 10)  # True
print(10 == 9)   # False

# 2. Not equal to (!=)
print("x != y:", x != y)  # True
print(10 != 10)  # False
print(10 != 9)   # True

# 3. Greater than (>)
print("x > y:", x > y)  # True

# 4. Less than (<)
print("x < y:", x < y)  # False

# 5. Greater than or equal to (>=)
print("x >= y:", x >= y)  # True

# 6. Less than or equal to (<=)
print("x <= y:", x <= y)  # False

# These operators work with numbers, strings, and other data types.

