# Set operations
set_a = {1, 2, 3, 4}
set_b = {3, 4, 5, 6}
print("Set A:", set_a)
print("Set B:", set_b)
print("Intersection (A & B):", set_a & set_b)  # Intersection
print("Union (A | B):", set_a | set_b)

print("Difference (A - B):", set_a - set_b)  # Difference
print("Symmetric Difference (A ^ B):", set_a ^ set_b)  # Symmetric Difference