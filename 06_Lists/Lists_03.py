"""
A fundamental data structure for organizing collections of items
"""


first_task = "Install Python"
second_task = "Learn Python"
third_task = "Take a break"

# Using a list to store multiple tasks
tasks = ["Install Python", "Learn Python", "Take a break"]

# Comma separates items in the list
# Square brackets [] define the list
print(tasks)  # Output: ['Install Python', 'Learn Python', 'Take a break']


tasks2 = [first_task, second_task, third_task]
print(tasks2)  # Output: ['Install Python', 'Learn Python', 'Take a break']