# Lists in Python: Why do we need them?
# -------------------------------------
# A list is a collection of items (elements)
# that can be stored together in a single variable.
# Lists are useful when you want to store,
# organize, and process multiple values efficiently.

# Basic Example: Storing multiple numbers
numbers = [10, 20, 30, 40, 50]
print("Numbers list:", numbers)

# Real World Scenario 1: Shopping list
shopping_list = ["Milk", "Eggs", "Bread", "Butter"]
print("Shopping list:", shopping_list)
# You can easily add, remove, or check items in your shopping list.

# Real World Scenario 2: Student names in a class
student_names = ["Alice", "Bob", "Charlie", "David"]
print("Student names:", student_names)
# You can loop through the list to mark attendance, send messages, or process grades.

# Summary:
# - Lists help you manage groups of related data.
# - You can access, modify, and process items easily using lists.
# - Lists are flexible and widely used in real-world programming tasks.

