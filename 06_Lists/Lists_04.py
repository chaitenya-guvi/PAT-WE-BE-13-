# Lists can hold multiple data types in Python
# -------------------------------------------
# Unlike arrays in some other languages,
# Python lists can store elements of different data types in the same list.

# Example 1: List with integers, floats, and strings
mixed_list = [10, 3.14, "hello"]
print("Mixed list:", mixed_list)  # Output: [10, 3.14, 'hello']

# Example 2: List with numbers, strings, and a boolean
varied_list = [42, "Python", True, 7.5]
print("Varied list:", varied_list)  # Output: [42, 'Python', True, 7.5]

# Example 3: List with different types including another list
complex_list = ["apple", 5, [1, 2, 3], False]
print("Complex list:", complex_list)  # Output: ['apple', 5, [1, 2, 3], False]

# Summary:
# - Python lists are flexible and can hold elements of any type, including other lists.
# - This makes lists useful for storing and processing mixed data in real-world programs.

