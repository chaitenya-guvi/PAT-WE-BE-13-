# What is a string in Python?
# A string is a sequence of characters enclosed in single (' '), double (" "),
# or triple quotes (''' ''' or """ """).
# Strings are used to represent text data and are immutable (cannot be changed after creation).

# Creating strings
single_quote_str = 'Hello, World! '
double_quote_str = "Python is fun!"
triple_quote_str = '''This is a multi-line
string example.'''


print(single_quote_str)
print(double_quote_str)
print(triple_quote_str)

