# Looping through a tuple
my_tuple = (1, 2, 3, "hello", True)
for item in my_tuple:
    print("Item:", item)

# Unpacking a tuple
a, b, c, d, e = my_tuple
print("Unpacked values:", a, b, c, d, e)
