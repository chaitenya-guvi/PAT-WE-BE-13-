# The if and else statements in Python are used for decision-making.
# 'if' checks a condition; if True, the indented block runs.
# 'else' runs if the condition is False.

# Example 1: Basic if-else
number = 4
if number > 5:
    print("The number is greater than 5.")
else:
    print("The number is not greater than 5.")
# Output: The number is not greater than 5.

# Example 2: Another if-else
name = "Alice"
if name == "Bob":
    print("Hello, Bob!")
else:
    print("Hello, stranger!")
# Output: Hello, stranger!

# The 'else' block is optional and only runs if the 'if' condition is False.

