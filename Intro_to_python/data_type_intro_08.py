# Data type conversion to string in Python
# Integer to string
num = 42
num_str = str(num)
print(num_str, type(num_str))

# Float to string
flt = 3.1415
flt_str = str(flt)
print(flt_str, type(flt_str))

# Boolean to string
flag = True
flag_str = str(flag)
print(flag_str, type(flag_str))

